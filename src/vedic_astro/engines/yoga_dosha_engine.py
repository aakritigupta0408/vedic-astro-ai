@dataclass(frozen=True)
class YogaResult:
    """
    Result of a yoga detection. Returned by each yoga detector function.
    Contains structured data only — prose is generated by the LLM layer.
    """
    name: str
    is_present: bool
    category: YogaCategory
    severity: Optional[YogaSeverity]       # None if not present
    strength: float                         # 0.0–1.0
    contributing_planets: list[PlanetName]
    key_houses: list[int]                   # Houses involved in this yoga
    effect_domain: list[str]               # e.g. ["career", "wealth"]
    source: str                             # Classical text reference
    conditions_met: list[str]              # Human-readable list of conditions satisfied
    conditions_failed: list[str]           # Conditions that weakened/prevented the yoga


@dataclass(frozen=True)
class DoshaResult:
    """
    Result of a dosha detection.
    """
    name: str
    is_present: bool
    category: DoshaCategory
    severity: float                         # 0.0–1.0 (0 = absent, 1 = full dosha)
    trigger_planets: list[PlanetName]
    trigger_houses: list[int]
    effect_domain: list[str]
    cancellation_factors: list[str]         # Classical cancellation rules met (mitigations)
    source: str


@dataclass
class YogaDoshaBundle:
    """
    Complete yoga and dosha analysis for a natal chart.
    Produced by detect_all_yogas_and_doshas() — the main public API.
    """
    chart_id: str
    yogas: list[YogaResult]
    doshas: list[DoshaResult]

    @property
    def active_yogas(self) -> list[YogaResult]:
        """Yogas that are present (is_present=True)."""
        return [y for y in self.yogas if y.is_present]

    @property
    def active_doshas(self) -> list[DoshaResult]:
        """Doshas that are present and not fully cancelled."""
        return [d for d in self.doshas if d.is_present and d.severity > 0.0]

    @property
    def net_yoga_strength(self) -> float:
        """Sum of yoga strengths (active yogas only), normalized to 0–1."""
        if not self.active_yogas:
            return 0.0
        total = sum(y.strength for y in self.active_yogas)
        return min(1.0, total / max(1, len(self.active_yogas)))

    @property
    def net_dosha_severity(self) -> float:
        """Average dosha severity (active doshas only)."""
        if not self.active_doshas:
            return 0.0
        return sum(d.severity for d in self.active_doshas) / len(self.active_doshas)


