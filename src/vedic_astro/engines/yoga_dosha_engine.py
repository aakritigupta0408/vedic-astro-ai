"""
yoga_dosha_engine.py — Yoga and Dosha detection engine.

Pure deterministic rule-based pattern matching — zero LLM calls.
Detects classical Parashari yogas (planetary combinations) and doshas
(afflictions) from a natal chart using structured rule evaluation.

Rule evaluation is fully deterministic: each yoga/dosha is defined by
a Python function that takes a NatalChart and returns a match result.

Yoga categories implemented:
    - Pancha Mahapurusha Yogas (Hamsa, Malavya, Ruchaka, Bhadra, Shasha)
    - Raj Yogas (Kendra-Kona lord connections)
    - Dhana Yogas (2nd/11th lord connections)
    - Gajakesari Yoga
    - Budhaditya Yoga
    - Chandra-Mangala Yoga
    - Amala Yoga
    - Vesi/Vosi/Ubhayachari Yogas

Dosha categories implemented:
    - Mangal Dosha (Kuja Dosha)
    - Kala Sarpa Dosha
    - Grahan Dosha (solar/lunar eclipse)
    - Kemdrum Yoga (Moon isolation dosha)
    - Guru Chandala Yoga (Jupiter-Rahu conjunction)
    - Shrapit Dosha (Saturn-Rahu conjunction)

All results are structured (not prose) — the Synthesis Agent uses these
structured results to build natural language descriptions.
"""

