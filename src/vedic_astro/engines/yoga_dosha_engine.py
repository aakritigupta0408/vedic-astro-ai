def _detect_mahapurusha(
    planet: PlanetName,
    yoga_name: str,
    chart: NatalChart,
    source: str,
    domains: list[str],
) -> YogaResult:
    """
    Generic detector for Pancha Mahapurusha Yogas.
    Condition: Planet must be in a Kendra house AND in exaltation or own sign.
    """
    in_kendra = _is_in_kendra(planet, chart)
    in_strength = _is_exalted_or_own(planet, chart)
    is_present = in_kendra and in_strength

    strength = _compute_yoga_strength(planet, chart) if is_present else 0.0
    severity = (
        YogaSeverity.STRONG if _dignity_of(planet, chart) == Dignity.EXALTED
        else YogaSeverity.MODERATE if is_present
        else None
    )

    met: list[str] = []
    failed: list[str] = []
    if in_kendra:
        met.append(f"{planet.value.title()} is in Kendra (house {_house_of(planet, chart)})")
    else:
        failed.append(f"{planet.value.title()} not in Kendra (house {_house_of(planet, chart)})")
    if in_strength:
        met.append(f"{planet.value.title()} is {_dignity_of(planet, chart).value}")
    else:
        failed.append(f"{planet.value.title()} is {_dignity_of(planet, chart).value} (not exalted/own)")

    return YogaResult(
        name=yoga_name,
        is_present=is_present,
        category=YogaCategory.MAHAPURUSHA,
        severity=severity,
        strength=strength,
        contributing_planets=[planet],
        key_houses=[_house_of(planet, chart)],
        effect_domain=domains,
        source=source,
        conditions_met=met,
        conditions_failed=failed,
    )


