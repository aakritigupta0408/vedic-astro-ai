def _house_of(planet: PlanetName, chart: NatalChart) -> int:
    """Return the house (1–12) that a planet occupies in the natal chart."""
    return chart.planets[planet].house


def _sign_of(planet: PlanetName, chart: NatalChart) -> int:
    """Return the sign (1–12) that a planet occupies."""
    return chart.planets[planet].sign_number


def _dignity_of(planet: PlanetName, chart: NatalChart) -> Dignity:
    """Return the dignity of a planet in the natal chart."""
    return chart.planets[planet].dignity


def _is_in_kendra(planet: PlanetName, chart: NatalChart) -> bool:
    """True if planet is in a Kendra house (1, 4, 7, 10)."""
    return _house_of(planet, chart) in (1, 4, 7, 10)


def _is_in_kona(planet: PlanetName, chart: NatalChart) -> bool:
    """True if planet is in a Kona (trine) house (1, 5, 9)."""
    return _house_of(planet, chart) in (1, 5, 9)


def _is_exalted_or_own(planet: PlanetName, chart: NatalChart) -> bool:
    """True if planet is in exaltation or own sign."""
    return _dignity_of(planet, chart) in (Dignity.EXALTED, Dignity.OWN, Dignity.MOOLATRIKONA)


def _lord_of_house(house: int, chart: NatalChart) -> PlanetName:
    """Return the planetary lord of a given house in the natal chart."""
    sign = chart.bhavas[house].sign_number
    return SIGN_LORDS[sign]


def _planets_in_house(house: int, chart: NatalChart) -> list[PlanetName]:
    """Return all planets occupying a given house."""
    return chart.bhavas[house].occupants


def _are_conjunct(p1: PlanetName, p2: PlanetName, chart: NatalChart) -> bool:
    """True if two planets are in the same sign (whole-sign conjunction)."""
    return _sign_of(p1, chart) == _sign_of(p2, chart)


def _house_distance(from_house: int, to_house: int) -> int:
    """1-based house distance: how many houses from from_house to to_house."""
    return ((to_house - from_house) % 12) + 1


def _aspects_house(planet: PlanetName, target_house: int, chart: NatalChart) -> bool:
    """
    Check if a planet aspects a given house via Vedic drishti.
    All planets aspect the 7th from themselves.
    Mars: also 4th and 8th. Jupiter: also 5th and 9th. Saturn: also 3rd and 10th.
    """
    planet_house = _house_of(planet, chart)
    special_aspects: dict[PlanetName, list[int]] = {
        PlanetName.MARS:    [4, 8],
        PlanetName.JUPITER: [5, 9],
        PlanetName.SATURN:  [3, 10],
    }
    offsets = [7] + special_aspects.get(planet, [])
    aspected_houses = [((planet_house - 1 + off - 1) % 12) + 1 for off in offsets]
    return target_house in aspected_houses


def _compute_yoga_strength(
    planet: PlanetName,
    chart: NatalChart,
    base_strength: float = 0.8,
) -> float:
    """
    Compute yoga strength based on planetary dignity.
    Exalted/Moolatrikona → full strength; Debilitated → minimal.
    """
    dignity_multipliers = {
        Dignity.EXALTED:      1.0,
        Dignity.MOOLATRIKONA: 0.9,
        Dignity.OWN:          0.8,
        Dignity.FRIEND:       0.65,
        Dignity.NEUTRAL:      0.5,
        Dignity.ENEMY:        0.35,
        Dignity.DEBILITATED:  0.15,
    }
    mult = dignity_multipliers.get(_dignity_of(planet, chart), 0.5)
    return round(base_strength * mult, 3)


