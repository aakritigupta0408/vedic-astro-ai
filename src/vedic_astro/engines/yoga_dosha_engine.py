@yoga_detector
def detect_gajakesari_yoga(chart: NatalChart) -> YogaResult:
    """
    Gajakesari Yoga — Jupiter in Kendra from Moon.
    Effects: wealth, fame, wisdom, good character.
    Source: BPHS ch. 36; Phaladeepika ch. 6
    """
    moon_house = _house_of(PlanetName.MOON, chart)
    jup_house  = _house_of(PlanetName.JUPITER, chart)
    dist = _house_distance(moon_house, jup_house)
    in_kendra_from_moon = dist in (1, 4, 7, 10)

    met: list[str] = []
    failed: list[str] = []

    if in_kendra_from_moon:
        met.append(f"Jupiter in {dist}th from Moon (Kendra from Moon)")
    else:
        failed.append(f"Jupiter in {dist}th from Moon — not Kendra")

    # Strengthen if both are in good dignity
    jup_strong = _is_exalted_or_own(PlanetName.JUPITER, chart)
    moon_strong = _dignity_of(PlanetName.MOON, chart) not in (Dignity.ENEMY, Dignity.DEBILITATED)
    if jup_strong:
        met.append(f"Jupiter is {_dignity_of(PlanetName.JUPITER, chart).value}")
    if moon_strong:
        met.append(f"Moon is {_dignity_of(PlanetName.MOON, chart).value}")
    else:
        failed.append(f"Moon is weakened ({_dignity_of(PlanetName.MOON, chart).value})")

    is_present = in_kendra_from_moon
    strength = 0.0
    if is_present:
        base = 0.7
        if jup_strong: base += 0.15
        if moon_strong: base += 0.10
        strength = min(1.0, base)

    severity = (
        YogaSeverity.STRONG if strength >= 0.85
        else YogaSeverity.MODERATE if strength >= 0.65
        else YogaSeverity.WEAK if is_present
        else None
    )

    return YogaResult(
        name="Gajakesari Yoga",
        is_present=is_present,
        category=YogaCategory.LUNAR_YOGA,
        severity=severity,
        strength=round(strength, 3),
        contributing_planets=[PlanetName.JUPITER, PlanetName.MOON],
        key_houses=[moon_house, jup_house],
        effect_domain=["wealth", "general", "career"],
        source="BPHS ch. 36; Phaladeepika ch. 6",
        conditions_met=met,
        conditions_failed=failed,
    )


@yoga_detector
def detect_budhaditya_yoga(chart: NatalChart) -> YogaResult:
    """
    Budhaditya Yoga — Sun and Mercury conjunct (same sign).
    Effects: intelligence, sharp intellect, writing, public recognition.
    Source: Phaladeepika ch. 6
    """
    conjunct = _are_conjunct(PlanetName.SUN, PlanetName.MERCURY, chart)
    sun_house = _house_of(PlanetName.SUN, chart)

    met: list[str] = []
    failed: list[str] = []

    if conjunct:
        met.append(f"Sun and Mercury conjunct in sign {_sign_of(PlanetName.SUN, chart)} (house {sun_house})")
    else:
        failed.append(
            f"Sun in house {sun_house}, Mercury in house {_house_of(PlanetName.MERCURY, chart)} — not conjunct"
        )

    # Quality check: Mercury combust (within 14° of Sun) weakens the yoga
    sun_lon = chart.planets[PlanetName.SUN].longitude
    mer_lon = chart.planets[PlanetName.MERCURY].longitude
    orb = abs(sun_lon - mer_lon)
    if orb > 180:
        orb = 360 - orb
    is_combust = orb <= 14.0

    strength = 0.0
    if conjunct:
        base = 0.75
        if is_combust:
            base -= 0.25
            failed.append(f"Mercury is combust (within {orb:.1f}° of Sun) — yoga weakened")
        if _dignity_of(PlanetName.MERCURY, chart) in (Dignity.EXALTED, Dignity.OWN, Dignity.MOOLATRIKONA):
            base += 0.15
            met.append(f"Mercury is {_dignity_of(PlanetName.MERCURY, chart).value} — yoga strengthened")
        strength = max(0.0, min(1.0, base))

    return YogaResult(
        name="Budhaditya Yoga",
        is_present=conjunct,
        category=YogaCategory.SOLAR_YOGA,
        severity=(
            YogaSeverity.STRONG if strength >= 0.8
            else YogaSeverity.MODERATE if strength >= 0.5
            else YogaSeverity.WEAK if conjunct
            else None
        ),
        strength=round(strength, 3),
        contributing_planets=[PlanetName.SUN, PlanetName.MERCURY],
        key_houses=[sun_house],
        effect_domain=["education", "career", "general"],
        source="Phaladeepika ch. 6",
        conditions_met=met,
        conditions_failed=failed,
    )


@yoga_detector
def detect_raj_yoga(chart: NatalChart) -> YogaResult:
    """
    Raja Yoga — Lord of a Kendra house and lord of a Kona house are connected
    (conjunct, mutual aspect, or exchange / parivartana).

    This is a composite detection for the most common form of Raja Yoga.
    Source: BPHS ch. 34
    """
    kendra_houses = [1, 4, 7, 10]
    kona_houses   = [1, 5, 9]

    kendra_lords = {chart.bhavas[h].lord for h in kendra_houses}
    kona_lords   = {chart.bhavas[h].lord for h in kona_houses}

    # Planets that lord both a kendra and kona (natural yogakarakas)
    yogakaraka_planets = kendra_lords & kona_lords
    # Remove Sun/Moon as lagna lords if lagna is 5th/9th sign from itself (trivial case)

    # Check connections between kendra lords and kona lords
    connections: list[tuple[PlanetName, PlanetName, str]] = []

    for kp in kendra_lords:
        for kop in kona_lords:
            if kp == kop:
                continue
            # Conjunction
            if _are_conjunct(kp, kop, chart):
                connections.append((kp, kop, "conjunction"))
            # Exchange (parivartana)
            elif (
                _sign_of(kp, chart) in _get_own_signs(kop)
                and _sign_of(kop, chart) in _get_own_signs(kp)
            ):
                connections.append((kp, kop, "parivartana"))

    is_present = bool(connections) or bool(yogakaraka_planets)
    met: list[str] = []
    failed: list[str] = []

    if yogakaraka_planets:
        for p in yogakaraka_planets:
            met.append(f"{p.value.title()} lords both Kendra and Kona — natural Yogakaraka")

    for kp, kop, conn_type in connections:
        met.append(
            f"Kendra lord {kp.value.title()} and Kona lord {kop.value.title()} in {conn_type}"
        )

    if not is_present:
        failed.append("No Kendra-Kona lord connection found")

    # Strength: based on number and quality of connections
    strength = 0.0
    if yogakaraka_planets:
        strength += 0.5 * min(1.0, len(yogakaraka_planets) / 2)
    if connections:
        strength += 0.5 * min(1.0, len(connections) / 3)
    strength = min(1.0, strength)

    involved_planets = list({p for kp, kop, _ in connections for p in [kp, kop]}
                            | yogakaraka_planets)

    return YogaResult(
        name="Raja Yoga",
        is_present=is_present,
        category=YogaCategory.RAJ_YOGA,
        severity=(
            YogaSeverity.STRONG if strength >= 0.8
            else YogaSeverity.MODERATE if strength >= 0.4
            else YogaSeverity.WEAK if is_present
            else None
        ),
        strength=round(strength, 3),
        contributing_planets=involved_planets,
        key_houses=kendra_houses + kona_houses,
        effect_domain=["career", "status", "general"],
        source="BPHS ch. 34",
        conditions_met=met,
        conditions_failed=failed,
    )


def _get_own_signs(planet: PlanetName) -> set[int]:
    """Return the set of signs owned by a planet."""
    return OWN_SIGNS.get(planet, set())


@yoga_detector
def detect_dhana_yoga(chart: NatalChart) -> YogaResult:
    """
    Dhana Yoga — Connection between 2nd and 11th house lords (wealth indicators).
    Also includes 5th lord (luck/speculation) and lagna lord.
    Source: BPHS ch. 32
    """
    lord_2  = _lord_of_house(2, chart)
    lord_11 = _lord_of_house(11, chart)
    lord_5  = _lord_of_house(5, chart)
    lord_1  = _lord_of_house(1, chart)

    # Check connections
    wealth_planets = [lord_2, lord_11, lord_5, lord_1]
    connections: list[str] = []

    if _are_conjunct(lord_2, lord_11, chart):
        connections.append(f"2nd lord ({lord_2.value}) conjunct 11th lord ({lord_11.value})")
    if _are_conjunct(lord_2, lord_5, chart):
        connections.append(f"2nd lord ({lord_2.value}) conjunct 5th lord ({lord_5.value})")
    if _house_of(lord_2, chart) == 11 or _house_of(lord_11, chart) == 2:
        connections.append(f"2nd/11th lord exchange or placement")
    if _house_of(lord_11, chart) in (1, 2, 5, 9, 11):
        connections.append(f"11th lord ({lord_11.value}) in auspicious house {_house_of(lord_11, chart)}")

    is_present = bool(connections)
    strength = min(1.0, len(connections) * 0.3) if is_present else 0.0

    # Boost if both 2nd and 11th lords are strong
    if _is_exalted_or_own(lord_2, chart) and _is_exalted_or_own(lord_11, chart):
        strength = min(1.0, strength + 0.2)

    return YogaResult(
        name="Dhana Yoga",
        is_present=is_present,
        category=YogaCategory.DHANA_YOGA,
        severity=(
            YogaSeverity.STRONG if strength >= 0.8
            else YogaSeverity.MODERATE if strength >= 0.4
            else YogaSeverity.WEAK if is_present
            else None
        ),
        strength=round(strength, 3),
        contributing_planets=[p for p in set(wealth_planets) if is_present],
        key_houses=[2, 11, 5],
        effect_domain=["wealth", "finance"],
        source="BPHS ch. 32",
        conditions_met=connections,
        conditions_failed=[] if is_present else ["No 2nd/11th lord connection found"],
    )


@yoga_detector
def detect_amala_yoga(chart: NatalChart) -> YogaResult:
    """
    Amala Yoga — A natural benefic planet occupies the 10th house from Lagna or Moon.
    Effects: fame, spotless reputation, good career.
    Source: Phaladeepika ch. 6
    """
    benefics = [PlanetName.JUPITER, PlanetName.VENUS, PlanetName.MERCURY, PlanetName.MOON]
    moon_house = _house_of(PlanetName.MOON, chart)

    benefics_in_10th_from_lagna = [
        p for p in benefics if _house_of(p, chart) == 10
    ]
    benefics_in_10th_from_moon = [
        p for p in benefics
        if _house_distance(moon_house, _house_of(p, chart)) == 10
    ]

    is_present = bool(benefics_in_10th_from_lagna or benefics_in_10th_from_moon)
    all_contributing = list(set(benefics_in_10th_from_lagna + benefics_in_10th_from_moon))

    met: list[str] = []
    if benefics_in_10th_from_lagna:
        met.append(f"Benefic(s) {[p.value for p in benefics_in_10th_from_lagna]} in 10th from Lagna")
    if benefics_in_10th_from_moon:
        met.append(f"Benefic(s) {[p.value for p in benefics_in_10th_from_moon]} in 10th from Moon")

    strength = min(1.0, len(all_contributing) * 0.4) if is_present else 0.0

    return YogaResult(
        name="Amala Yoga",
        is_present=is_present,
        category=YogaCategory.SPECIAL,
        severity=YogaSeverity.STRONG if strength >= 0.7 else YogaSeverity.MODERATE if is_present else None,
        strength=round(strength, 3),
        contributing_planets=all_contributing,
        key_houses=[10],
        effect_domain=["career", "reputation"],
        source="Phaladeepika ch. 6",
        conditions_met=met,
        conditions_failed=[] if is_present else ["No natural benefic in 10th from Lagna or Moon"],
    )


@yoga_detector
def detect_vesi_vosi_yoga(chart: NatalChart) -> YogaResult:
    """
    Vesi Yoga: Planets (other than Moon) in 2nd from Sun → intelligent, wealthy.
    Vosi Yoga: Planets (other than Moon) in 12th from Sun → scholarly.
    Ubhayachari Yoga: Planets on BOTH sides → kingly qualities.
    Source: Phaladeepika ch. 6
    """
    sun_house = _house_of(PlanetName.SUN, chart)
    house_after_sun  = (sun_house % 12) + 1        # 2nd from Sun
    house_before_sun = ((sun_house - 2) % 12) + 1  # 12th from Sun

    planets_after  = [
        p for p in PlanetName
        if p not in (PlanetName.SUN, PlanetName.MOON, PlanetName.RAHU, PlanetName.KETU)
        and _house_of(p, chart) == house_after_sun
    ]
    planets_before = [
        p for p in PlanetName
        if p not in (PlanetName.SUN, PlanetName.MOON, PlanetName.RAHU, PlanetName.KETU)
        and _house_of(p, chart) == house_before_sun
    ]

    has_vesi  = bool(planets_after)
    has_vosi  = bool(planets_before)
    has_ubhay = has_vesi and has_vosi

    if has_ubhay:
        yoga_name = "Ubhayachari Yoga"
    elif has_vesi:
        yoga_name = "Vesi Yoga"
    elif has_vosi:
        yoga_name = "Vosi Yoga"
    else:
        yoga_name = "Vesi/Vosi Yoga"

    is_present = has_vesi or has_vosi
    contributing = planets_after + planets_before
    met: list[str] = []
    if planets_after:
        met.append(f"Planets in 2nd from Sun (Vesi): {[p.value for p in planets_after]}")
    if planets_before:
        met.append(f"Planets in 12th from Sun (Vosi): {[p.value for p in planets_before]}")

    return YogaResult(
        name=yoga_name,
        is_present=is_present,
        category=YogaCategory.SOLAR_YOGA,
        severity=YogaSeverity.STRONG if has_ubhay else YogaSeverity.MODERATE if is_present else None,
        strength=0.7 if has_ubhay else (0.5 if is_present else 0.0),
        contributing_planets=contributing,
        key_houses=[sun_house, house_after_sun, house_before_sun],
        effect_domain=["career", "general"],
        source="Phaladeepika ch. 6",
        conditions_met=met,
        conditions_failed=[] if is_present else ["No planets in 2nd or 12th from Sun"],
    )


