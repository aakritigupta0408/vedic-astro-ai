class YogaCategory(str, Enum):
    MAHAPURUSHA = "mahapurusha"
    RAJ_YOGA    = "raj_yoga"
    DHANA_YOGA  = "dhana_yoga"
    LUNAR_YOGA  = "lunar_yoga"
    SOLAR_YOGA  = "solar_yoga"
    SPECIAL     = "special"


class DoshaCategory(str, Enum):
    MANGAL     = "mangal_dosha"
    KALA_SARPA = "kala_sarpa_dosha"
    GRAHAN     = "grahan_dosha"
    ISOLATION  = "isolation_dosha"
    CURSE      = "curse_dosha"


class YogaSeverity(str, Enum):
    STRONG   = "strong"    # All conditions met perfectly
    MODERATE = "moderate"  # Core conditions met, some weakening factors
    WEAK     = "weak"      # Conditions partially met


