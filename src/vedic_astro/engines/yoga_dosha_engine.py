@dosha_detector
def detect_mangal_dosha(chart: NatalChart) -> DoshaResult:
    """
    Mangal Dosha (Kuja Dosha) — Mars in houses 1, 2, 4, 7, 8, or 12.
    Primarily affects marriage; severity varies by house and sign.
    Source: Phaladeepika; Muhurta Chintamani

    Classical cancellations reduce severity:
        - Mars in own/exaltation sign
        - Mars in Aries or Scorpio in the dosha house
        - Lagna is Aries, Scorpio, or Capricorn
        - Venus occupies Lagna
        - Mars in 2nd of Gemini or Virgo
        - Both partners have Mangal Dosha (cancels out in matching)
    """
    mars_house = _house_of(PlanetName.MARS, chart)
    mangal_houses = {1, 2, 4, 7, 8, 12}
    is_present = mars_house in mangal_houses

    # Severity by house (classical weighting: 7th is worst, then 8th, then 1st)
    severity_map = {7: 1.0, 8: 0.9, 1: 0.8, 4: 0.7, 12: 0.6, 2: 0.5}
    base_severity = severity_map.get(mars_house, 0.0)

    cancellations: list[str] = []
    severity_reduction = 0.0

    if is_present:
        mars_dignity = _dignity_of(PlanetName.MARS, chart)
        if mars_dignity in (Dignity.EXALTED, Dignity.OWN, Dignity.MOOLATRIKONA):
            cancellations.append(f"Mars in {mars_dignity.value} — Dosha partially cancelled")
            severity_reduction += 0.3

        mars_sign = _sign_of(PlanetName.MARS, chart)
        if mars_sign in (1, 8):  # Aries or Scorpio (own signs)
            cancellations.append("Mars in own sign (Aries/Scorpio) — Dosha cancelled")
            severity_reduction += 0.4

        if chart.lagna_sign in (1, 8, 10):  # Aries, Scorpio, Capricorn lagna
            cancellations.append(f"Lagna is {chart.lagna_sign_name} — Dosha significantly reduced")
            severity_reduction += 0.3

        venus_in_lagna = _house_of(PlanetName.VENUS, chart) == 1
        if venus_in_lagna:
            cancellations.append("Venus in Lagna — Dosha reduced")
            severity_reduction += 0.2

        # Jupiter aspecting Mars
        if _aspects_house(PlanetName.JUPITER, mars_house, chart):
            cancellations.append("Jupiter aspects Mars — Dosha reduced")
            severity_reduction += 0.2

    final_severity = max(0.0, min(1.0, base_severity - severity_reduction)) if is_present else 0.0

    return DoshaResult(
        name="Mangal Dosha (Kuja Dosha)",
        is_present=is_present,
        category=DoshaCategory.MANGAL,
        severity=round(final_severity, 3),
        trigger_planets=[PlanetName.MARS],
        trigger_houses=[mars_house] if is_present else [],
        effect_domain=["marriage", "relationships"],
        cancellation_factors=cancellations,
        source="Phaladeepika; Muhurta Chintamani",
    )


@dosha_detector
def detect_kala_sarpa_dosha(chart: NatalChart) -> DoshaResult:
    """
    Kala Sarpa Dosha — All 7 classical planets (not Rahu/Ketu) are hemmed
    between Rahu and Ketu (within the arc from Rahu to Ketu in one direction).

    Source: Disputed — not in BPHS; popular in modern Parashari practice.
    Note: Classified as debated dosha; included for completeness.
    """
    rahu_lon = chart.planets[PlanetName.RAHU].longitude
    ketu_lon = chart.planets[PlanetName.KETU].longitude

    # Arc from Rahu to Ketu going forward (Rahu → Ketu clockwise)
    # All planets must fall within this arc
    classical_planets = [
        PlanetName.SUN, PlanetName.MOON, PlanetName.MARS,
        PlanetName.MERCURY, PlanetName.JUPITER, PlanetName.VENUS, PlanetName.SATURN,
    ]

    def in_arc(planet_lon: float, start: float, end: float) -> bool:
        """Check if planet_lon is in the arc from start to end (going forward 0–360)."""
        if start <= end:
            return start <= planet_lon <= end
        else:  # arc crosses 0°
            return planet_lon >= start or planet_lon <= end

    # Two arcs: Rahu→Ketu and Ketu→Rahu
    # Check if all planets fall in the Rahu→Ketu arc
    arc_rahu_to_ketu_end = ketu_lon
    arc_rahu_to_ketu_start = rahu_lon

    planets_in_rahu_ketu_arc = [
        p for p in classical_planets
        if in_arc(chart.planets[p].longitude, arc_rahu_to_ketu_start, arc_rahu_to_ketu_end)
    ]
    planets_in_ketu_rahu_arc = [
        p for p in classical_planets
        if in_arc(chart.planets[p].longitude, ketu_lon, rahu_lon)
    ]

    full_kala_sarpa = len(planets_in_rahu_ketu_arc) == 7
    full_kala_amrita = len(planets_in_ketu_rahu_arc) == 7  # reverse = Kala Amrita (mixed effects)

    is_present = full_kala_sarpa or full_kala_amrita

    # Cancellations: any planet outside the Rahu-Ketu axis
    cancellations: list[str] = []
    if not is_present and len(planets_in_rahu_ketu_arc) >= 5:
        cancellations.append("Partial Kala Sarpa — not all planets hemmed")

    severity = 0.8 if full_kala_sarpa else (0.6 if full_kala_amrita else 0.0)

    return DoshaResult(
        name="Kala Sarpa Dosha" if full_kala_sarpa else "Kala Amrita Dosha" if full_kala_amrita else "Kala Sarpa Dosha",
        is_present=is_present,
        category=DoshaCategory.KALA_SARPA,
        severity=round(severity, 3),
        trigger_planets=[PlanetName.RAHU, PlanetName.KETU],
        trigger_houses=[
            _house_of(PlanetName.RAHU, chart),
            _house_of(PlanetName.KETU, chart),
        ],
        effect_domain=["general", "career", "health"],
        cancellation_factors=cancellations,
        source="Modern Parashari tradition (not in BPHS — debated)",
    )


@dosha_detector
def detect_grahan_dosha(chart: NatalChart) -> DoshaResult:
    """
    Grahan Dosha (Eclipse Dosha) — Sun or Moon conjunct Rahu or Ketu (same sign).
    Affects health, longevity, and general well-being.
    Source: BPHS ch. 81
    """
    shadow_planets = [PlanetName.RAHU, PlanetName.KETU]
    luminaries = [PlanetName.SUN, PlanetName.MOON]

    afflicted_pairs: list[tuple[PlanetName, PlanetName]] = []
    for luminary in luminaries:
        for shadow in shadow_planets:
            if _are_conjunct(luminary, shadow, chart):
                afflicted_pairs.append((luminary, shadow))

    is_present = bool(afflicted_pairs)
    severity_base = min(1.0, len(afflicted_pairs) * 0.5)

    trigger_planets: list[PlanetName] = []
    trigger_houses: list[int] = []
    cancellations: list[str] = []

    for luminary, shadow in afflicted_pairs:
        trigger_planets.extend([luminary, shadow])
        trigger_houses.append(_house_of(luminary, chart))

    trigger_planets = list(set(trigger_planets))
    trigger_houses = list(set(trigger_houses))

    # Jupiter aspecting the afflicted luminary reduces dosha
    for luminary, _ in afflicted_pairs:
        if _aspects_house(PlanetName.JUPITER, _house_of(luminary, chart), chart):
            cancellations.append(f"Jupiter aspects {luminary.value} — Dosha reduced")
            severity_base *= 0.6

    domains = []
    if any(p == PlanetName.SUN for p, _ in afflicted_pairs):
        domains.append("health")
        domains.append("father")
    if any(p == PlanetName.MOON for p, _ in afflicted_pairs):
        domains.append("mental_health")
        domains.append("mother")

    return DoshaResult(
        name="Grahan Dosha",
        is_present=is_present,
        category=DoshaCategory.GRAHAN,
        severity=round(severity_base, 3),
        trigger_planets=trigger_planets,
        trigger_houses=trigger_houses,
        effect_domain=domains or ["general"],
        cancellation_factors=cancellations,
        source="BPHS ch. 81",
    )


@dosha_detector
def detect_kemdrum_yoga(chart: NatalChart) -> DoshaResult:
    """
    Kemdrum Yoga (Moon isolation dosha) — Moon has no planet in the 2nd or 12th
    from it (in the sign immediately before or after Moon's sign), and no planet
    conjunct Moon. Causes poverty, hardship, and mental instability.

    Note: Kemdrum is technically a "yoga" in classical texts but functions as a dosha.
    Source: BPHS ch. 37
    """
    moon_house = _house_of(PlanetName.MOON, chart)
    second_from_moon  = (moon_house % 12) + 1
    twelfth_from_moon = ((moon_house - 2) % 12) + 1

    non_lunar_planets = [
        p for p in PlanetName
        if p not in (PlanetName.MOON, PlanetName.RAHU, PlanetName.KETU)
    ]

    planets_adjacent = [
        p for p in non_lunar_planets
        if _house_of(p, chart) in (second_from_moon, twelfth_from_moon, moon_house)
    ]

    is_present = len(planets_adjacent) == 0

    cancellations: list[str] = []
    if not is_present:
        for p in planets_adjacent:
            cancellations.append(
                f"{p.value.title()} in house {_house_of(p, chart)} — Kemdrum cancelled"
            )

    # Additional cancellation: Moon in Kendra from Lagna
    if _house_of(PlanetName.MOON, chart) in (1, 4, 7, 10):
        if is_present:
            cancellations.append("Moon in Kendra from Lagna — Kemdrum partially mitigated")
            is_present = False  # Full cancellation per classical texts

    severity = 0.75 if is_present else 0.0

    return DoshaResult(
        name="Kemdrum Yoga",
        is_present=is_present,
        category=DoshaCategory.ISOLATION,
        severity=round(severity, 3),
        trigger_planets=[PlanetName.MOON],
        trigger_houses=[moon_house],
        effect_domain=["mental_health", "general", "wealth"],
        cancellation_factors=cancellations,
        source="BPHS ch. 37",
    )


